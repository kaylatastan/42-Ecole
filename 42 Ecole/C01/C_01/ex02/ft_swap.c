void	ft_swap(int *a, int *b)
{
	int	tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}

#include <stdio.h>

int main()
{
	int x = 9, y = 3;

	ft_swap(&x, &y);
	printf("%d | %d", x, y);
}
