void	ft_sort_int_tab(int *tab, int size)
{
	int	a;
	int	b;

	a = 0;
	size--;
	while (a < size)
	{
		if (tab[a] > tab[a + 1])
		{
			b = tab[a];
			tab [a] = tab [a + 1];
			tab [a + 1] = b;
			a = 0;
		}
		else
			a++;
	}
}
